    public void carregaTabelaProdutos(String tipo){       //metodo responsavel por preencher a tabela com os dados dos produtos
        Object linha[] = new Object[]{0, 
                    "", 
                    0,
                    "",
                    0
                };
        
        modeloProdutos.setRowCount(0);
        for (Map.Entry<Integer, Produto> set : administrador.getEntrySetProdutos()) { //laço responsavel por preencher a tabela usando como base os tipos de produtos
            if(tipo.equals(set.getValue().getTipo())){
                linha = new Object[]{set.getValue().getCodigo(), 
                    set.getValue().getNome(), 
                    set.getValue().getQuantidade(),
                    set.getValue().getMarca(),
                    set.getValue().getPreço()
                };
                modeloProdutos.addRow(linha);
            }
            if(tipo.equals("Todos")){
                linha = new Object[]{
                    set.getValue().getCodigo(), 
                    set.getValue().getNome(), 
                    set.getValue().getQuantidade(),
                    set.getValue().getMarca(),
                    set.getValue().getPreço()
                };
                modeloProdutos.addRow(linha);
            }
        }
        tabela_Produtos.setModel(modeloProdutos);
        tabela_Produtos.getColumnModel().getColumn(0).setPreferredWidth(20);
        tabela_Produtos.getColumnModel().getColumn(1).setPreferredWidth(100);
        tabela_Produtos.getColumnModel().getColumn(2).setPreferredWidth(50);
        tabela_Produtos.getColumnModel().getColumn(3).setPreferredWidth(80);
        tabela_Produtos.getColumnModel().getColumn(4).setPreferredWidth(35);
    }
    
 
