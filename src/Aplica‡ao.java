    private void jButton1_comprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_comprarActionPerformed
        try {                           //vai realizar a compra
            if(cliente.gerarCompraCarrinho(administrador)){     //verifica as quantidades presentes no carrinho
                Compra compra = new Compra(cliente.getUsuario());       //cria nova compra
                compra.setProdutos(cliente.getCarrinho());              //vai adicionar os produtos no carrinho
                administrador.addCompra(compra.getGeradorOrdem(), compra);      //adiciona a compra no adm                           
                JOptionPane.showMessageDialog(null, "Compra Realizado Com Sucesso!", null, HEIGHT);
                cliente.carrinhoSetNovaQtd();                               
                Arquivo.escreverAdm(administrador);                     //escrever no arquivo o que foi alterado no adm
                
            }else{
                JOptionPane.showMessageDialog(null, "Compra não Realizada", null, HEIGHT);
            }
        } catch (QuantidadeInsuficienteException ex) {
            JOptionPane.showMessageDialog(null, "Quantidade insuficiente.", null, HEIGHT);
        }
        cliente.cancelarCompraCliente();                            //limpar carrinho do cliente
        tabela_Compras.setModel(cliente.carregaTelaCompraCarrinho());
        jFormattedTextField1_preçototal.setText("R$ "+cliente.getTotalCarrinho());
        carregaTabelaProdutos("Todos");
//        Arquivo.
    }//GEN-LAST:event_jButton1_comprarActionPerformed

