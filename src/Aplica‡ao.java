    private void jButton2_adicionaCarrinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_adicionaCarrinhoActionPerformed
        int index = tabela_Produtos.getSelectedRow(); //responsavel por pegar a linha da tebela que o usuario escolheu
        if (index != -1){                             //caso ele não tenha escolhido nenhuma linha não entra nesse if
            int codigo = (int) modeloProdutos.getValueAt(index, 0); //pega o cogido do produto daquela linha selecionada
            if (textQuantidade.getText().compareTo("") != 0){
                int qtd = Integer.parseInt(textQuantidade.getText());
                if(administrador.verificaQtd(qtd, codigo)){
                    try {                                   //vai ser responsavel por adicionar o item escolhido no carrinho
                        cliente.getCarrinhoCli().addProduto(administrador.getProdutosCodigo(codigo), qtd);
                        tabela_Compras.setModel(cliente.carregaTelaCompraCarrinho());
                        jFormattedTextField1_preçototal.setText("R$ "+cliente.getTotalCarrinho());
                        JOptionPane.showMessageDialog(null, "Produto adicionado ao carrinho.", null, HEIGHT);
                    } catch (ValorIncoerenteException ex) {
                        JOptionPane.showMessageDialog(null, "Valor inválido!", null, HEIGHT);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Quantidade indisponivel!", null, HEIGHT);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Selecione a quantidade", null, HEIGHT);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto!", null, HEIGHT);
        }
    }//GEN-LAST:event_jButton2_adicionaCarrinhoActionPerformed

