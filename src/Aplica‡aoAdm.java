    private void jButton2_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_alterarActionPerformed
        int index = jTable1_cadastro.getSelectedRow();      //pega a linha da tabela
        if(index != -1){
            int aux = (int)jTable1_cadastro.getValueAt(index, 0);       //pega o valor no campo especifico
            j.setProduto(adm.getProdutosCodigo(aux));               //carrega o produto na tabela de alterar o produto
            j.setVisible(true);
            j.mostrarDados(adm.getProdutosCodigo(aux));
            j.codigo = aux;
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um item na tabela", null, HEIGHT);
        }
        
        
    }//GEN-LAST:event_jButton2_alterarActionPerformed

