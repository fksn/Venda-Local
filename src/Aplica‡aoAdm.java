    private void jButton4_informaçoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4_informaçoesActionPerformed
        String t;
        int index = jTable2_compras.getSelectedRow();//pega a linha 
        int i  = (int)jTable2_compras.getValueAt(index, 1);//pega o codigo
        if(index != -1){
            
//            carregarCompras();
            compra = adm.getCompra(i); //pega a compra com aquele código
            //abaixo é o responsavel por mostrar a compra que foi escolhida
            t = "Usuario: " + compra.getUsuario()+"\n";
            t += "Ordem de Compra: " + compra.getOrdemCompra() + "\n\nProdutos: ";
            
            for (Map.Entry<Produto, Integer> set : compra.getEntrySetCompra()) {
                t += "\nNome: " + set.getKey().getNome();
                t += "\nMarca: " + set.getKey().getMarca();
                t += "\nQuantidade: " + set.getValue();
                t += "\nPreço por unidade: " + set.getKey().getPreço()+ "\n";
            }

            jTextArea1_informaçoes.setText(t);
        }
    }//GEN-LAST:event_jButton4_informaçoesActionPerformed

