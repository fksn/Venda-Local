    private void jButton6_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6_salvarActionPerformed
        //salva o produto
        if(jTextField1_nomeProd.getText().equals("")){
            JOptionPane.showMessageDialog(null, "É Obrigatório preencher o campo 'Nome'", null, HEIGHT);
            return;
        }
        else if(jComboBox1.getSelectedItem().toString().equals("Tipo")){
            JOptionPane.showMessageDialog(null, "É Obrigatório preencher o campo 'Tipo'", null, HEIGHT);
            return;
        }
        else if(jTextField3_quantProd.getText().equals("")){
            JOptionPane.showMessageDialog(null, "É Obrigatório preencher o campo 'Quantidade'", null, HEIGHT);
            return;
        }
        else if(jTextField4_marcaProd.getText().equals("")){
            JOptionPane.showMessageDialog(null, "É Obrigatório preencher o campo 'Marca'", null, HEIGHT);
            return;
        }
        else if(jTextField1_preço.getText().equals("")){
            JOptionPane.showMessageDialog(null, "É Obrigatório preencher o campo 'Preço'", null, HEIGHT);
            return;
        }
        try {
            Produto p = new Produto( //cria novo produto
                    jTextField1_nomeProd.getText(),
                    Integer.parseInt(textCodigo.getText()),
                    jComboBox1.getSelectedItem().toString(),
                    Integer.parseInt(jTextField3_quantProd.getText()),
                    Double.parseDouble(jTextField1_preço.getText()),
                    jTextField4_marcaProd.getText()
            );
            if(adm.addProduto(Integer.parseInt(textCodigo.getText()), p)){//adiciona o produto no adm
                JOptionPane.showMessageDialog(null, "Cadastro Realizado Com Sucesso!", null, HEIGHT);
            }else{
                JOptionPane.showMessageDialog(null, "Cadastro não Realizado, código já utilizado!", null, HEIGHT);
            }
        } catch (ValorIncoerenteException ex) {
            JOptionPane.showMessageDialog(null, "Valores inválidos!", null, HEIGHT);
        }
        jTextField1_preço.setEditable(false);
        textCodigo.setEditable(false);
        jTextField1_nomeProd.setEditable(false);
        jComboBox1.setEnabled(false);
        jTextField3_quantProd.setEditable(false);
        jTextField4_marcaProd.setEditable(false);
        textCodigo.setText("");
        jTextField1_preço.setText("");
        jTextField1_nomeProd.setText("");
        jComboBox1.setSelectedIndex(0);
        jTextField3_quantProd.setText("");
        jTextField4_marcaProd.setText("");

        carregaTabelaProdutos();
    }//GEN-LAST:event_jButton6_salvarActionPerformed

