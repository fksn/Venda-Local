    public void carregaTabelaCompras(){   //responsavel por realizar o preenchimento da tabela de compras
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Usuario","Ordem de Compra"},0);
        
        for (Map.Entry<Integer, Compra> set : adm.getEntrySetCompras()) 
        {
            Object linha[] = new Object[]{set.getValue().getUsuario(), set.getValue().getOrdemCompra()};
            modelo.addRow(linha);
        }
        jTable2_compras.setModel(modelo);
        
    }
    
    public void carregaTabelaProdutos(){  //responsavel por realizar o preenchimento da tabela de produtos
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"codigo","Nome","Quantidade","Marca","Preço"},0);        
        
        for (Map.Entry<Integer, Produto> set : adm.getEntrySetProdutos()) {
            Object linha[] = new Object[]{set.getValue().getCodigo(),
                set.getValue().getNome(),
                set.getValue().getQuantidade(),
                set.getValue().getMarca(),
                set.getValue().getPreço()
                };
            modelo.addRow(linha);
        }
        jTable1_cadastro.setModel(modelo);
        jTable1_cadastro.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTable1_cadastro.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTable1_cadastro.getColumnModel().getColumn(2).setPreferredWidth(50);
        jTable1_cadastro.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTable1_cadastro.getColumnModel().getColumn(4).setPreferredWidth(35);
    }
    
